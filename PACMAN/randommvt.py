def GetAdjacentNodes(i):        # Return adjacent Nodes of Node i, diagonal are ignored here
	nodes = []
	if i[0]-1 >=  0:
		nodes += [(i[0]-1, i[1])]
	if i[0]+1 <=  map_height:
		nodes += [(i[0]+1, i[1])]
	if i[1]-1 >= 0:
		nodes += [(i[0], i[1]-1)]
	if i[1]+1 <= map_width:
		nodes += [(i[0], i[1]+1)]
	return nodes

